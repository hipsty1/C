#include <stdio.h>

int main(){
    int i;
    printf("Looping For\n");
    for(i=0; i<4; i++){
        printf("Hallo\n");
    }
    printf("Done");
    return 0;
}

//sebuah looping dengan format for(inisialisasi; kondisi; in/decrement){statement}
